/**
Toan tu         Vi du           Tuong duong
=               x = y           x = y
+=              x += y          x = x + y
-=              x -= y          x = x - y
*=              x *= y          x = x * y
/=              x /= y          x = x / y
**=             x **= y         x = x ** y


 */

var a = 10;

a = 5;
console.log('=> a = ' + a);

a += 10
