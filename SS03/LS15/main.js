/**
 * Toan tu so hoc
 +      --> Cong
 -      --> Tru
 *      --> Nhan
 **     --> Luy thua
 /      --> chia 
 %      --> Chia lay du
 ++     --> Tang len 1 don vi
 --     --> Giam di 1 don vi
 
 */

var a = 20;
var b = 3;

console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a ** b);
console.log(a / b);
console.log(a % b);
console.log(++b); //tang b len 1 don vi roi in ra console
console.log(b++); //in b ra console roi moi tang b len 1 don vi

console.log(b--); //in b ra console roi moi giam b di 1 don vi
console.log(--b); //giam b di 1 don vi roi in b ra console
